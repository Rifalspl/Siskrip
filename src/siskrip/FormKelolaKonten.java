    private void tampilkanKonten() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Judul");
    model.addColumn("Tipe");
    model.addColumn("Isi");
    model.addColumn("File/Link");
    model.addColumn("Update");

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM konten ORDER BY tgl_update DESC";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_konten"),
                rs.getString("judul"),
                rs.getString("tipe"),
                rs.getString("isi"),
                rs.getString("file_link"),
                rs.getString("tgl_update")
            });
        }
        tblKonten.setModel(model);
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan konten: " + ex.getMessage());
    }
}


