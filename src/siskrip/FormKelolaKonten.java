    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String judul = txtJudul.getText().trim();
    String tipe = cmbTipe.getSelectedItem().toString();
    String isi = txtIsi.getText().trim();
    String fileLink = txtFileLink.getText().trim();
    String tglUpdate = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new java.util.Date());

    if (judul.isEmpty() || tipe.isEmpty() || isi.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi (kecuali file/link)!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO konten (judul, tipe, isi, file_link, tgl_update) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, judul);
        pst.setString(2, tipe);
        pst.setString(3, isi);
        pst.setString(4, fileLink);
        pst.setString(5, tglUpdate);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Konten berhasil ditambah!");
        tampilkanKonten();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal tambah konten: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

    int row = tblKonten.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih konten yang akan diedit!");
        return;
    }
    String id = tblKonten.getValueAt(row, 0).toString();
    String judul = txtJudul.getText().trim();
    String tipe = cmbTipe.getSelectedItem().toString();
    String isi = txtIsi.getText().trim();
    String fileLink = txtFileLink.getText().trim();
    String tglUpdate = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new java.util.Date());

    if (judul.isEmpty() || tipe.isEmpty() || isi.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi (kecuali file/link)!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "UPDATE konten SET judul=?, tipe=?, isi=?, file_link=?, tgl_update=? WHERE id_konten=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, judul);
        pst.setString(2, tipe);
        pst.setString(3, isi);
        pst.setString(4, fileLink);
        pst.setString(5, tglUpdate);
        pst.setString(6, id);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Konten berhasil diupdate!");
        tampilkanKonten();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal update konten: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    int row = tblKonten.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih konten yang akan dihapus!");
        return;
    }
    String id = tblKonten.getValueAt(row, 0).toString();

    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus konten ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi != JOptionPane.YES_OPTION) return;

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "DELETE FROM konten WHERE id_konten=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, id);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Konten berhasil dihapus!");
        tampilkanKonten();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal hapus konten: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton3ActionPerformed

