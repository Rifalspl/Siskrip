    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
    int row = tblProgres.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih progres yang ingin didownload dokumennya!");
        return;
    }
    String namaFile = tblProgres.getValueAt(row, 5).toString(); // index ke-5
    if (namaFile == null || namaFile.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Data dokumen tidak ditemukan pada progres ini!");
        return;
    }
    String pathFile = "uploads/" + namaFile;
    try {
        java.awt.Desktop.getDesktop().open(new java.io.File(pathFile));
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Gagal membuka file: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

