    private void tampilkanProgresMahasiswa(String nim) {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Tahap");
    model.addColumn("Status");
    model.addColumn("Keterangan");
    model.addColumn("Tanggal Update");
    model.addColumn("Dokumen");

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT p.id_progres, p.tahap, p.status, p.keterangan, p.tgl_update, d.nama_file " +
                     "FROM progres_skripsi p " +
                     "LEFT JOIN dokumen_skripsi d ON p.nim = d.nim " +
                     "WHERE p.nim = ? " +
                     "ORDER BY p.tgl_update DESC";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nim);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_progres"),
                rs.getString("tahap"),
                rs.getString("status"),
                rs.getString("keterangan"),
                rs.getString("tgl_update"),
                rs.getString("nama_file")
            });
        }
        tblProgres.setModel(model);
        rs.close();
        pst.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan progres: " + ex.getMessage());
    }
}
    private void tampilkanProgresMahasiswaDariFile(String nim) {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Tahap");
    model.addColumn("Status");
    model.addColumn("Keterangan");
    model.addColumn("Tanggal");
    model.addColumn("Dokumen");

    try (BufferedReader br = new BufferedReader(new FileReader("progres.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] data = line.split("\\|");
            if (data.length >= 6 && data[0].equals(nim)) {
                model.addRow(new String[]{
                    data[1], // tahap
                    data[2], // status
                    data[3], // keterangan
                    data[4], // tanggal
                    data[5]  // dokumen
                });
            }
        }
        tblProgres.setModel(model);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Gagal membaca file progres: " + ex.getMessage());
    }
}


              


