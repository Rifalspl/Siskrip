    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

    int row = tblTopik.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data topik yang akan dihapus!");
        return;
    }
    String id = tblTopik.getValueAt(row, 0).toString();

    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus topik ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi != JOptionPane.YES_OPTION) return;

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "DELETE FROM topik WHERE id_topik=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, id);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Topik berhasil dihapus!");
        tampilkanTopik();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal hapus topik: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton4ActionPerformed

