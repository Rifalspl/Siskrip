    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    int row = tblTopik.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data topik yang akan diedit!");
        return;
    }
    String id = tblTopik.getValueAt(row, 0).toString();
    String judul = txtJudulTopik.getText().trim();
    String status = cmbStatus.getSelectedItem().toString();

    if (judul.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Judul topik harus diisi!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "UPDATE topik SET judul_topik=?, status=? WHERE id_topik=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, judul);
        pst.setString(2, status);
        pst.setString(3, id);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Topik berhasil diupdate!");
        tampilkanTopik();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal update topik: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton3ActionPerformed

