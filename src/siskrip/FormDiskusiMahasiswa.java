    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    int konfirmasi = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus SEMUA pesan di forum diskusi?",
            "Konfirmasi Hapus Semua", JOptionPane.YES_NO_OPTION);
    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "DELETE FROM diskusi";
            Statement stmt = conn.createStatement();
            stmt.executeUpdate(sql);
            stmt.close();

            JOptionPane.showMessageDialog(this, "Semua pesan berhasil dihapus!");
            tampilkanDiskusi();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus semua pesan: " + ex.getMessage());
        }
    }

    }//GEN-LAST:event_jButton1ActionPerformed

