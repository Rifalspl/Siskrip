   private void tampilkanDiskusi() {
    txtAreaDiskusi.setText("");
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM diskusi ORDER BY tanggal ASC";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            String baris = rs.getString("tanggal") + " - " +
                           rs.getString("nama_pengirim") + ": " +
                           rs.getString("isi_pesan") + "\n";
            txtAreaDiskusi.append(baris);
        }
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        txtAreaDiskusi.setText("Gagal menampilkan diskusi: " + ex.getMessage());
    }
}



