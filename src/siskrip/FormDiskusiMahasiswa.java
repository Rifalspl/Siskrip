    private void btnKirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKirimActionPerformed

    String nama_pengirim = txtNama.getText().trim();
    String nim = txtNIM.getText().trim();
    String pesan = txtPesan.getText().trim();

    if (nama_pengirim.isEmpty() || nim.isEmpty() || pesan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama, NIM, dan Pesan wajib diisi!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO diskusi (nim, nama_pengirim, isi_pesan) VALUES (?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nim);
        pst.setString(2, nama_pengirim);
        pst.setString(3, pesan);

        pst.executeUpdate();
        pst.close();

        txtPesan.setText("");
        tampilkanDiskusi();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal mengirim pesan: " + ex.getMessage());
    }

    
    }//GEN-LAST:event_btnKirimActionPerformed

