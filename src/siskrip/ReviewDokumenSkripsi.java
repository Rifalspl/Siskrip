    private void tampilkanDokumen() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("NIM");
    model.addColumn("Nama File");
    model.addColumn("Path File");
    model.addColumn("Tanggal Upload");
    model.addColumn("Status");

    String cariNIM = txtCariNIM.getText().trim();
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM dokumen_skripsi";
        if (!cariNIM.isEmpty()) {
            sql += " WHERE nim = ?";
        }
        sql += " ORDER BY tanggal_upload DESC";
        PreparedStatement pst = conn.prepareStatement(sql);
        if (!cariNIM.isEmpty()) {
            pst.setString(1, cariNIM);
        }
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_dokumen"),
                rs.getString("nim"),
                rs.getString("nama_file"),
                rs.getString("path_file"),
                rs.getDate("tanggal_upload"),
                rs.getString("status")
            });
        }
        tblDokumen.setModel(model);
        rs.close();
        pst.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan dokumen: " + ex.getMessage());
    }
}
    
 



       

   
    
    



