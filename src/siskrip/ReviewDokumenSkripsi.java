    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int row = tblDokumen.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih dokumen mahasiswa terlebih dahulu!");
        return;
    }
    String idDokumen = tblDokumen.getValueAt(row, 0).toString();
    String isiUmpan = txtUmpanBalik.getText().trim();
    String namaDosen = "Dosen Satu"; // Bisa diganti sesuai login

    if (isiUmpan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Isi umpan balik tidak boleh kosong!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO umpan_balik (id_dokumen, tanggal_umpan, isi_umpan, dosen) VALUES (?, NOW(), ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idDokumen);
        pst.setString(2, isiUmpan);
        pst.setString(3, namaDosen);

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Umpan balik berhasil dikirim!");
        txtUmpanBalik.setText("");
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal mengirim umpan balik: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton6ActionPerformed

