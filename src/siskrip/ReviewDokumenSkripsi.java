    private void tampilkanUmpanBalikDokumen(String idDokumen) {
       txtUmpanBalik.setText(""); // Pastikan txtUmpanBalik adalah nama JTextArea yang kamu pakai!
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM umpan_balik WHERE id_dokumen=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idDokumen);
        ResultSet rs = pst.executeQuery();

        int no = 1;
        while (rs.next()) {
            String baris =
                no + ". Tanggal: " + rs.getDate("tanggal_umpan") + "\n" +
                "   Dosen: " + rs.getString("dosen") + "\n" +
                "   Umpan Balik: " + rs.getString("isi_umpan") + "\n\n";
            txtUmpanBalik.append(baris);
            no++;
        }
        if (no == 1) {
            txtUmpanBalik.setText("Belum ada umpan balik untuk dokumen ini.");
        }
        rs.close();
        pst.close();
    } catch (SQLException ex) {
        txtUmpanBalik.setText("Gagal menampilkan umpan balik: " + ex.getMessage());
    }
    }
