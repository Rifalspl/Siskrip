    private void btnTerimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerimaActionPerformed
        int row = tblJadwal.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih jadwal dulu!");
        return;
    }
    String idJadwal = tblJadwal.getValueAt(row, 0).toString();
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "UPDATE jadwal_bimbingan SET status='Diterima' WHERE id_jadwal=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idJadwal);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Jadwal sudah diterima.");
        tampilkanJadwal();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal mengubah status: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnTerimaActionPerformed

