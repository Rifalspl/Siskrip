    private void tampilkanJadwal() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("NIM");
    model.addColumn("Tanggal");
    model.addColumn("Waktu");
    model.addColumn("Keterangan");
    model.addColumn("Status");

    String cariNIM = txtCariNIM.getText().trim(); // Kalau ada fitur pencarian NIM

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM jadwal_bimbingan";
        if (!cariNIM.isEmpty()) {
            sql += " WHERE nim = ?";
        }
        sql += " ORDER BY tanggal DESC, waktu ASC";

        PreparedStatement pst = conn.prepareStatement(sql);
        if (!cariNIM.isEmpty()) {
            pst.setString(1, cariNIM);
        }

        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_jadwal"),
                rs.getString("nim"),
                rs.getDate("tanggal"),
                rs.getString("waktu"),
                rs.getString("keterangan"),
                rs.getString("status")
            });
        }
        tblJadwal.setModel(model);
        rs.close();
        pst.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan jadwal: " + ex.getMessage());
    }
}


