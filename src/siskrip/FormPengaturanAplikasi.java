    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    tampilkanPreviewPengaturan();
    String namaApp = txtNamaApp.getText().trim();
    String namaKampus = txtNamaKampus.getText().trim();
    String alamat = txtAlamat.getText().trim();
    String email = txtEmail.getText().trim();
    String telepon = txtTelepon.getText().trim();
    String logoPath = txtLogoPath.getText().trim();
    String tema = cmbTema.getSelectedItem().toString();

    try {
        Connection conn = Koneksi.getConnection();
        // Cek apakah sudah ada pengaturan
        String cek = "SELECT COUNT(*) FROM pengaturan_aplikasi";
        Statement s = conn.createStatement();
        ResultSet r = s.executeQuery(cek);
        r.next();
        int jumlah = r.getInt(1);
        r.close(); s.close();

        if (jumlah == 0) {
            // INSERT baru
            String sql = "INSERT INTO pengaturan_aplikasi (nama_aplikasi, nama_kampus, alamat, email, telepon, logo_path, tema) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaApp);
            pst.setString(2, namaKampus);
            pst.setString(3, alamat);
            pst.setString(4, email);
            pst.setString(5, telepon);
            pst.setString(6, logoPath);
            pst.setString(7, tema);
            pst.executeUpdate();
            pst.close();
        } else {
            // UPDATE
            String sql = "UPDATE pengaturan_aplikasi SET nama_aplikasi=?, nama_kampus=?, alamat=?, email=?, telepon=?, logo_path=?, tema=? LIMIT 1";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaApp);
            pst.setString(2, namaKampus);
            pst.setString(3, alamat);
            pst.setString(4, email);
            pst.setString(5, telepon);
            pst.setString(6, logoPath);
            pst.setString(7, tema);
            pst.executeUpdate();
            pst.close();
        }
        JOptionPane.showMessageDialog(this, "Pengaturan berhasil disimpan!");
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal simpan pengaturan: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

