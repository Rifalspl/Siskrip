    private void tampilkanPengaturan() {
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM pengaturan_aplikasi LIMIT 1";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        if (rs.next()) {
            txtNamaApp.setText(rs.getString("nama_aplikasi"));
            txtNamaKampus.setText(rs.getString("nama_kampus"));
            txtAlamat.setText(rs.getString("alamat"));
            txtEmail.setText(rs.getString("email"));
            txtTelepon.setText(rs.getString("telepon"));
            txtLogoPath.setText(rs.getString("logo_path"));
            cmbTema.setSelectedItem(rs.getString("tema"));
        }
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal tampilkan pengaturan: " + ex.getMessage());
    }
}
    
    private void tampilkanPreviewPengaturan() {
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM pengaturan_aplikasi LIMIT 1";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        if (rs.next()) {
            String hasil =
                "Nama Aplikasi : " + rs.getString("nama_aplikasi") + "\n" +
                "Nama Kampus   : " + rs.getString("nama_kampus") + "\n" +
                "Alamat        : " + rs.getString("alamat") + "\n" +
                "Email         : " + rs.getString("email") + "\n" +
                "Telepon       : " + rs.getString("telepon") + "\n" +
                "Logo Path     : " + rs.getString("logo_path") + "\n" +
                "Tema          : " + rs.getString("tema") + "\n";
            txtPreview.setText(hasil);
        } else {
            txtPreview.setText("Belum ada pengaturan tersimpan.");
        }
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        txtPreview.setText("Gagal memuat preview: " + ex.getMessage());
    }
}



