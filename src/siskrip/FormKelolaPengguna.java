    private void tampilkanPengguna() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Username");
    model.addColumn("Nama");
    model.addColumn("Tipe");
    model.addColumn("NIM");
    model.addColumn("NIDN");

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT id_pengguna, username, nama, tipe, nim, nidn FROM pengguna";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_pengguna"),
                rs.getString("username"),
                rs.getString("nama"),
                rs.getString("tipe"),
                rs.getString("nim"),
                rs.getString("nidn")
            });
        }
        tblPengguna.setModel(model);
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan pengguna: " + ex.getMessage());
    }
}


