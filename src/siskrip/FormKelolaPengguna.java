    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

    int row = tblPengguna.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data pengguna yang akan dihapus!");
        return;
    }
    String id = tblPengguna.getValueAt(row, 0).toString();

    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus pengguna ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi != JOptionPane.YES_OPTION) return;

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "DELETE FROM pengguna WHERE id_pengguna=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, id);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Pengguna berhasil dihapus!");
        tampilkanPengguna();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal hapus pengguna: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton4ActionPerformed

