    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        if (selectedFile == null) {
        JOptionPane.showMessageDialog(this, "Silakan pilih file terlebih dahulu!");
        return;
    }
    String nim = txtNIM.getText().trim();
    if (nim.isEmpty()) {
        JOptionPane.showMessageDialog(this, "NIM tidak boleh kosong!");
        return;
    }

    try {
        // Buat folder uploads jika belum ada
        String uploadDir = "uploads";
        File destDir = new File(uploadDir);
        if (!destDir.exists()) destDir.mkdir();

        // Copy file ke folder uploads
        String newFilePath = uploadDir + "/" + selectedFile.getName();
        java.nio.file.Files.copy(
            selectedFile.toPath(),
            new File(newFilePath).toPath(),
            java.nio.file.StandardCopyOption.REPLACE_EXISTING
        );

        // Simpan info file ke database
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO dokumen_skripsi (nim, nama_file, path_file, tanggal_upload, status) VALUES (?, ?, ?, CURDATE(), 'Menunggu')";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nim);
        pst.setString(2, selectedFile.getName());
        pst.setString(3, newFilePath);

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Dokumen berhasil diupload!");
        txtNamaFile.setText("");
        selectedFile = null;
        if (txtDaftarDokumen != null) {
            tampilkanDaftarDokumen(); // Kalau kamu pakai textarea untuk daftar dokumen
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal upload: " + e.getMessage());
    }

      

    }//GEN-LAST:event_btnUploadActionPerformed

