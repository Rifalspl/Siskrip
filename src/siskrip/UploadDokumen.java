    private void tampilkanDaftarDokumen() {
    txtDaftarDokumen.setText(""); // Kosongkan dulu
    String nim = txtNIM.getText().trim();
    if (nim.isEmpty()) {
        txtDaftarDokumen.setText("Isi NIM terlebih dahulu!");
        return;
    }
    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM dokumen_skripsi WHERE nim = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nim);
        ResultSet rs = pst.executeQuery();

        int no = 1;
        while (rs.next()) {
            String baris = 
                no + ". Nama File: " + rs.getString("nama_file") + "\n" +
                "   Path: " + rs.getString("path_file") + "\n" +
                "   Tanggal Upload: " + rs.getDate("tanggal_upload") + "\n" +
                "   Status: " + rs.getString("status") + "\n\n";
            txtDaftarDokumen.append(baris);
            no++;
        }
        if (no == 1) {
            txtDaftarDokumen.setText("Belum ada dokumen untuk NIM ini.");
        }
        rs.close();
        pst.close();
    } catch (Exception ex) {
        txtDaftarDokumen.setText("Gagal menampilkan dokumen: " + ex.getMessage());
    }
}

          

