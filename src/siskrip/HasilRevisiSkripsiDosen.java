    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

    String nim = txtNIM.getText().trim();
    String namaFile = txtNamaFile.getText().trim();
    String pathFile = "uploads/" + namaFile;
    String tanggalUpload = new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());
    String status = "Menunggu"; // status awal

    if (nim.isEmpty() || namaFile.isEmpty() || selectedFile == null) {
        JOptionPane.showMessageDialog(this, "Lengkapi NIM dan pilih file revisi!");
        return;
    }

    try {
        // Copy file ke folder uploads/
        File uploadsDir = new File("uploads");
        if (!uploadsDir.exists()) uploadsDir.mkdir();

        File destFile = new File(uploadsDir, namaFile);
        java.nio.file.Files.copy(selectedFile.toPath(), destFile.toPath(), java.nio.file.StandardCopyOption.REPLACE_EXISTING);

        // Simpan data ke database
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO dokumen_skripsi (nim, nama_file, path_file, tanggal_upload, status) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nim);
        pst.setString(2, namaFile);
        pst.setString(3, pathFile);
        pst.setString(4, tanggalUpload);
        pst.setString(5, status);
        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Revisi skripsi berhasil di-upload!");
        tampilkanDokumen();
        txtNamaFile.setText("");
        selectedFile = null;
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Gagal upload: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton2ActionPerformed

