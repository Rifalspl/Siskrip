    private void tampilkanDokumen() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("NIM");
    model.addColumn("Nama File");
    model.addColumn("Path File");
    model.addColumn("Tanggal Upload");
    model.addColumn("Status");
    model.addColumn("Nilai");
    model.addColumn("Status Validasi");
    model.addColumn("Catatan Validasi");

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT * FROM dokumen_skripsi ORDER BY tanggal_upload DESC";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_dokumen"),
                rs.getString("nim"),
                rs.getString("nama_file"),
                rs.getString("path_file"),
                rs.getString("tanggal_upload"),
                rs.getString("status"),
                rs.getString("nilai"),
                rs.getString("status_validasi"),
                rs.getString("catatan_validasi")
            });
        }
        tblDokumen.setModel(model);
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan dokumen: " + ex.getMessage());
    }
}


