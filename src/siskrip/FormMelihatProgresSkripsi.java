    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    int row = tblProgres.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data progres yang ingin dihapus!");
        return;
    }
    String idProgres = tblProgres.getValueAt(row, 0).toString();

    int konfirmasi = JOptionPane.showConfirmDialog(this,
        "Yakin ingin menghapus data progres ini?",
        "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "DELETE FROM progres_skripsi WHERE id_progres = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, idProgres);
            pst.executeUpdate();
            pst.close();

            JOptionPane.showMessageDialog(this, "Data progres berhasil dihapus!");
            tampilkanProgres();
            txtDetailProgres.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus progres: " + ex.getMessage());
        }
    }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

    int row = tblProgres.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data progres yang ingin didownload!");
        return;
    }
    String namaFile = tblProgres.getValueAt(row, 7).toString(); // Kolom ke-8 = nama file/dokumen

    // Cek jika namaFile null/kosong
    if (namaFile == null || namaFile.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Data dokumen tidak ditemukan pada progres ini!");
        return;
    }
    String pathFile = "uploads/" + namaFile;

    // Debug: cek nama file yang diambil
    System.out.println("File yang dicoba di-download: " + pathFile);

    try {
        java.awt.Desktop.getDesktop().open(new java.io.File(pathFile));
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Gagal membuka file: " + ex.getMessage());
    }



    }//GEN-LAST:event_jButton4ActionPerformed

