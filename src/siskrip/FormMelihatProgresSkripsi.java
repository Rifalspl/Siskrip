    private void tampilkanProgres() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("NIM");
    model.addColumn("Nama");
    model.addColumn("Tahap");
    model.addColumn("Status");
    model.addColumn("Keterangan");
    model.addColumn("Tanggal Update");
    model.addColumn("Dokumen"); // Tambahan kolom dokumen

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "SELECT p.id_progres, p.nim, m.nama, p.tahap, p.status, p.keterangan, p.tgl_update, d.nama_file " +
                     "FROM progres_skripsi p " +
                     "JOIN mahasiswa m ON p.nim = m.nim " +
                     "LEFT JOIN dokumen_skripsi d ON p.nim = d.nim " +
                     "ORDER BY p.tgl_update DESC";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_progres"),
                rs.getString("nim"),
                rs.getString("nama"),
                rs.getString("tahap"),
                rs.getString("status"),
                rs.getString("keterangan"),
                rs.getString("tgl_update"),
                rs.getString("nama_file")   // <-- dokumen skripsi
            });
        }
        tblProgres.setModel(model);
        rs.close();
        stmt.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan progres: " + ex.getMessage());
    }
}

            
