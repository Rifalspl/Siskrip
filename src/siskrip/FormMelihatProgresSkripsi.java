    String status = cmbStatus.getSelectedItem().toString();
    String keterangan = txtKeterangan.getText().trim();

    if(nim.isEmpty() || tahap.isEmpty() || status.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Lengkapi data progres!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();
        String sql = "INSERT INTO progres_skripsi (nim, tahap, status, keterangan) VALUES (?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nim);
        pst.setString(2, tahap);
        pst.setString(3, status);
        pst.setString(4, keterangan);

        pst.executeUpdate();
        pst.close();

        JOptionPane.showMessageDialog(this, "Data progres berhasil ditambah!");
        tampilkanProgres();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Gagal tambah progres: " + ex.getMessage());
    }

