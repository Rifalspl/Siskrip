    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String nim = txtNIM.getText().trim();

    if (fileTerpilih == null || nim.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Isi NIM dan pilih file terlebih dahulu!");
        return;
    }

    try {
        // Buat folder uploads jika belum ada
        File folder = new File("uploads");
        if (!folder.exists()) {
            folder.mkdir();
        }

        // Salin file ke folder uploads
        String namaFile = fileTerpilih.getName();
        File tujuan = new File("uploads/" + namaFile);
        Files.copy(fileTerpilih.toPath(), tujuan.toPath(), StandardCopyOption.REPLACE_EXISTING);

        // Simpan info ke database
        String sql = "INSERT INTO dokumen_skripsi (id_mahasiswa, nama_file, path_file) VALUES (?, ?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, nim);
        ps.setString(2, namaFile);
        ps.setString(3, tujuan.getPath());
        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Dokumen berhasil diupload!");
        tampilkanDokumen();
        txtFileInfo.setText("");
        fileTerpilih = null;

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal upload: " + e.getMessage());
    }

    
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int baris = tabelDokumen.getSelectedRow();
    String nim = txtNIM.getText().trim();

    if (baris == -1 || nim.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Pilih dokumen dan isi NIM terlebih dahulu!");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(
        this, "Hapus dokumen ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            String namaFile = tabelDokumen.getValueAt(baris, 0).toString();

            // Ambil path file
            String path = "";
            String sqlPath = "SELECT path_file FROM dokumen_skripsi WHERE nama_file = ? AND id_mahasiswa = ?";
            PreparedStatement psCari = conn.prepareStatement(sqlPath);
            psCari.setString(1, namaFile);
            psCari.setString(2, nim);
            ResultSet rs = psCari.executeQuery();

            if (rs.next()) {
                path = rs.getString("path_file");
            }

            // Hapus file fisik
            File file = new File(path);
            if (file.exists()) {
                file.delete();
            }

            // Hapus dari database
            String sqlHapus = "DELETE FROM dokumen_skripsi WHERE nama_file = ? AND id_mahasiswa = ?";
            PreparedStatement psHapus = conn.prepareStatement(sqlHapus);
            psHapus.setString(1, namaFile);
            psHapus.setString(2, nim);
            psHapus.executeUpdate();

            JOptionPane.showMessageDialog(null, "Dokumen berhasil dihapus.");
            tampilkanDokumen();
            txtFileInfo.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal hapus dokumen: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

