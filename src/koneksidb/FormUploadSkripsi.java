    public void tampilkanDokumen() {
    String nim = txtNIM.getText().trim();
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Nama File");
    model.addColumn("Tanggal Upload");

    try {
        String sql = "SELECT nama_file, tanggal_upload FROM dokumen_skripsi WHERE id_mahasiswa = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, nim);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nama_file"),
                rs.getDate("tanggal_upload")
            });
        }

        tabelDokumen.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan dokumen: " + e.getMessage());
    }
}
