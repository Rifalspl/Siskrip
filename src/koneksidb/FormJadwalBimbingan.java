    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int baris = tabelJadwal.getSelectedRow();

    if (baris == -1) {
        JOptionPane.showMessageDialog(null, "Pilih jadwal yang ingin dihapus terlebih dahulu!");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(
        null,
        "Yakin ingin menghapus jadwal ini?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION
    );

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            // Ambil tanggal dan jam dari baris terpilih
            String tanggal = tabelJadwal.getValueAt(baris, 0).toString();
            String jam = tabelJadwal.getValueAt(baris, 1).toString();

            String sql = "DELETE FROM jadwal_bimbingan WHERE id_mahasiswa = ? AND tanggal = ? AND jam = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, "2304411409"); // Ganti sesuai ID login
            ps.setString(2, tanggal);
            ps.setString(3, jam);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Jadwal berhasil dihapus.");
            tampilkanJadwal();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menghapus jadwal: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

