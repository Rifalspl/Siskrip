public void tampilkanJadwal() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Tanggal");
    model.addColumn("Jam");
    model.addColumn("Status");

    String nim = txtNIM.getText().trim();

    try {
        String sql = "SELECT tanggal, jam, status FROM jadwal_bimbingan WHERE id_mahasiswa = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, nim);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getDate("tanggal"),
                rs.getString("jam"),
                rs.getString("status")
            });
        }

        tabelJadwal.setModel(model);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal tampilkan jadwal: " + e.getMessage());
    }
}
public void simpanJadwal() {
    String nim = txtNIM.getText().trim();
    java.util.Date tanggal = dateChooser.getDate();
    String jam = comboJam.getSelectedItem() != null ? comboJam.getSelectedItem().toString() : "";

    if (nim.isEmpty() || tanggal == null || jam.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
        return;
    }

    try {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tglSQL = sdf.format(tanggal);

        String sql = "INSERT INTO jadwal_bimbingan (id_mahasiswa, tanggal, jam) VALUES (?, ?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, nim);
        ps.setString(2, tglSQL);
        ps.setString(3, jam);
        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Jadwal berhasil diajukan.");
        tampilkanJadwal();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal simpan jadwal: " + e.getMessage());
    }
}
